CREATE DATABASE IF NOT EXISTS 36_derniere_heure CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE 36_derniere_heure;

CREATE TABLE joueur (
    id            INT AUTO_INCREMENT PRIMARY KEY,
    pseudo        VARCHAR(50)  NOT NULL,
    points_de_vie INT          NOT NULL DEFAULT 100,
    sante_mentale INT          NOT NULL DEFAULT 100,
    score         INT          NOT NULL DEFAULT 0,
    blessure      TINYINT(1)   NOT NULL DEFAULT 0,
    griffure      TINYINT(1)   NOT NULL DEFAULT 0,
    preuve        TINYINT(1)   NOT NULL DEFAULT 0,
    date_debut    DATETIME     NOT NULL DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE page (
    id       INT PRIMARY KEY,
    titre    VARCHAR(150) NOT NULL,
    contenu  TEXT         NOT NULL,
    est_fin  TINYINT(1)   NOT NULL DEFAULT 0,
    type_fin ENUM('victoire','defaite','neutre') DEFAULT NULL,
    acte     TINYINT      NOT NULL DEFAULT 1
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE choix (
    id                  INT AUTO_INCREMENT PRIMARY KEY,
    page_source_id      INT  NOT NULL,
    page_destination_id INT  NOT NULL,
    texte_choix         TEXT NOT NULL,
    condition_objet     VARCHAR(100) DEFAULT NULL,
    condition_flag      VARCHAR(20)  DEFAULT NULL,
    degat_vie           INT  NOT NULL DEFAULT 0,
    degat_mental        INT  NOT NULL DEFAULT 0,
    FOREIGN KEY (page_source_id)      REFERENCES page(id) ON DELETE CASCADE,
    FOREIGN KEY (page_destination_id) REFERENCES page(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE objet (
    id          INT AUTO_INCREMENT PRIMARY KEY,
    nom         VARCHAR(100) NOT NULL,
    description TEXT         NOT NULL,
    type        ENUM('outil','arme','cle','document') NOT NULL DEFAULT 'outil'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE inventaire (
    id            INT AUTO_INCREMENT PRIMARY KEY,
    joueur_id     INT      NOT NULL,
    objet_id      INT      NOT NULL,
    date_collecte DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (joueur_id) REFERENCES joueur(id) ON DELETE CASCADE,
    FOREIGN KEY (objet_id)  REFERENCES objet(id)  ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE progression (
    id               INT AUTO_INCREMENT PRIMARY KEY,
    joueur_id        INT      NOT NULL,
    page_actuelle_id INT      NOT NULL,
    horodatage       DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (joueur_id)        REFERENCES joueur(id) ON DELETE CASCADE,
    FOREIGN KEY (page_actuelle_id) REFERENCES page(id)   ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

